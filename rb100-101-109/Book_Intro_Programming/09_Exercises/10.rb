# Hash values can be any data type, including arrays.

hsh1 = {kids: ["Joe", "Ben", "Jerry"]} 
hsh1 # => {:kids=>["Joe", "Ben", "Jerry"]}

# Array elements can be any type, including hashes.

arr = [{kid1: "Joe"}, {kid2: "Ben"}]
arr # => [{:kid1=>"Joe"}, {:kid2=>"Ben"}]

